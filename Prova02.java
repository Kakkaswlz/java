/******************************************************************************
2) Escreva um programa em Java que imprima os números de 1 a 10 
usando o laço for.
*******************************************************************************/
public class Prova02
{
	public static void main(String[] args) {
	  
	  for (int i = 0; i <= 10; i++){
	      System.out.println(i);
	  }
	}
}
